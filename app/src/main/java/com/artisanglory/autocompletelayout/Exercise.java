public class Exercise {
    private String name;
    private double calorie;

    public Exercise(String name, double calorie) {
        this.name = name;
        this.calorie = calorie;
    }

    public String getName() {
        return name;
    }

    public double getCalorie() {
        return calorie;
    }
}
